NSMutableArray *vocList;


- (void)viewDidLoad {
    [super viewDidLoad];
    NSString *pth = [[NSBundle mainBundle] pathForResource:@"vocab" ofType:@"plist"];
    vocList = [NSMutableArray arrayWithContentsOfFile:pth];
    
    
