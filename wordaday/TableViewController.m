#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [vocList count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"VocabCell" forIndexPath:indexPath];
    
    NSDictionary *vocDic = [vocList objectAtIndex:indexPath.row];
    cell.textLabel.text = [[[vocDic objectForKey:@"question"] stringByAppendingString:@" - "] stringByAppendingString:[vocDic objectForKey:@"answer"]];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%@ Wrong %@ Right",[vocDic objectForKey:@"wrong"],[vocDic objectForKey:@"right"]];
    return cell;
}


